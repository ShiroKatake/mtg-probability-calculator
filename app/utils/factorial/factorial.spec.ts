import { factorial } from './factorial';

describe('Factorial calculator', () => {
  it('0! and 1! should return 1', () => {
    expect(factorial(1)).toBe(1n);
    expect(factorial(0)).toBe(1n);
  });

  // Calculate for a deck of 60 cards
  it('60! should return 8320987112741390144276341183223364380754172606361245952449277696409600000000000000', () => {
    expect(factorial(60)).toBe(8320987112741390144276341183223364380754172606361245952449277696409600000000000000n);
  });

  // Calculate for a deck of 99 cards
  it('99! should return 933262154439441526816992388562667004907159682643816214685929638952175999932299156089414639761565182862536979208272237582511852109168640000000000000000000000', () => {
    expect(factorial(99)).toBe(933262154439441526816992388562667004907159682643816214685929638952175999932299156089414639761565182862536979208272237582511852109168640000000000000000000000n);
  });
});
